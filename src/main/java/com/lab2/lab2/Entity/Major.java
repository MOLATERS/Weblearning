package com.lab2.lab2.Entity;

public class Major {
    private String mname;
    private String mid;

    public Major() {
    }

    public Major(String mname, String mid) {
        this.mname = mname;
        this.mid = mid;
    }

    public String getMname() {
        return mname;
    }

    public void setMname(String mname) {
        this.mname = mname;
    }

    public String getMid() {
        return mid;
    }

    public void setMid(String mid) {
        this.mid = mid;
    }


}
